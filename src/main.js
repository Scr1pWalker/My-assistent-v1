// ─────────────────────────────────────────────
// 📌 main.js — точка входа всего приложения
//    Запускается первым. Здесь мы:
//    1. Создаём Vue-приложение
//    2. Подключаем плагины (роутер, хранилище)
//    3. "Монтируем" приложение в <div id="app">
// ─────────────────────────────────────────────

import { createApp } from 'vue'   // Фабрика Vue-приложения
import { createPinia } from 'pinia' // Хранилище данных (глобальный state)
import router from './router'       // Навигация между экранами
import App from './App.vue'         // Корневой компонент-оболочка
import './style.css'                // Глобальные стили + Tailwind

const app = createApp(App)  // Создаём приложение из корневого компонента

app.use(createPinia())  // Регистрируем хранилище
app.use(router)         // Регистрируем роутер

app.mount('#app')       // Вставляем приложение в div#app из index.html
